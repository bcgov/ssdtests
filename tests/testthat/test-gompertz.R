  sdata <- data.frame(left = x, right = x, weight = 1)
  pars <- estimates(fit$gompertz)

  expect_snapshot({
    set.seed(94)
    ssdtools:::sgompertz(sdata)
    set.seed(94)
    ssdtools:::sgompertz(sdata, pars)
